        public  IntPtr launchProcesses(String strFilePath, String strArgs)    //Возвращает дескриптор главного окна процесса
        {
            Process process = new Process();
            ProcessStartInfo startInfo = new ProcessStartInfo();
            startInfo.FileName = strFilePath;
            startInfo.Arguments = strArgs;
            process.StartInfo = startInfo;
            process.Start();
            IntPtr name = FindWindow(null, "Конфигурация");
            return name;

        }

        public IntPtr launchProcesses(String strFilePath, String strArgs, string namebase)    //Возвращает дескриптор главного окна процесса
        {
            Process process = new Process();
            ProcessStartInfo startInfo = new ProcessStartInfo();
            startInfo.FileName = strFilePath;
            startInfo.Arguments =  strArgs;
            process.StartInfo = startInfo;
            process.Start();
            while (IsWindow(FindWindow(null,namebase)) == false)
            {
                Thread.Sleep(1);
            }
            IntPtr name = FindWindow(null, namebase);
            return name;
        }

        [DllImport("user32.dll")]
        [return: MarshalAs(UnmanagedType.Bool)]
        static extern bool IsWindow(IntPtr hWnd);

        [DllImport("user32.dll", SetLastError = true)]
        static extern IntPtr FindWindow(string lpClassName, string lpWindowName);

        public void moveWindowToMonitors(int nMonitor, IntPtr nProcessWnd)
        {
            RECT windowRec;
            HandleRef hWnd = new HandleRef(null, nProcessWnd);
            GetWindowRect(hWnd, out windowRec);
            SetWindowPos(nProcessWnd,
                (IntPtr)SpecialWindowHandles.HWND_TOPMOST,
                Screen.AllScreens[nMonitor].WorkingArea.Left,
                Screen.AllScreens[nMonitor].WorkingArea.Top,
                windowRec.Width, // + 16,
                windowRec.Height + 24, // + 38,
                SetWindowPosFlags.ShowWindow);
        }

        [StructLayout(LayoutKind.Sequential)]
        public struct RECT
        {
            public int Left, Top, Right, Bottom;

            public RECT(int left, int top, int right, int bottom)
            {
                Left = left;
                Top = top;
                Right = right;
                Bottom = bottom;
            }

            public RECT(System.Drawing.Rectangle r) : this(r.Left, r.Top, r.Right, r.Bottom) { }

            public int X
            {
                get { return Left; }
                set { Right -= (Left - value); Left = value; }
            }

            public int Y
            {
                get { return Top; }
                set { Bottom -= (Top - value); Top = value; }
            }

            public int Height
            {
                get { return Bottom - Top; }
                set { Bottom = value + Top; }
            }

            public int Width
            {
                get { return Right - Left; }
                set { Right = value + Left; }
            }

            public System.Drawing.Point Location
            {
                get { return new System.Drawing.Point(Left, Top); }
                set { X = value.X; Y = value.Y; }
            }

            public System.Drawing.Size Size
            {
                get { return new System.Drawing.Size(Width, Height); }
                set { Width = value.Width; Height = value.Height; }
            }

            public static implicit operator System.Drawing.Rectangle(RECT r)
            {
                return new System.Drawing.Rectangle(r.Left, r.Top, r.Width, r.Height);
            }

            public static implicit operator RECT(System.Drawing.Rectangle r)
            {
                return new RECT(r);
            }

            public static bool operator ==(RECT r1, RECT r2)
            {
                return r1.Equals(r2);
            }

            public static bool operator !=(RECT r1, RECT r2)
            {
                return !r1.Equals(r2);
            }

            public bool Equals(RECT r)
            {
                return r.Left == Left && r.Top == Top && r.Right == Right && r.Bottom == Bottom;
            }

            public override bool Equals(object obj)
            {
                if (obj is RECT)
                    return Equals((RECT)obj);
                else if (obj is System.Drawing.Rectangle)
                    return Equals(new RECT((System.Drawing.Rectangle)obj));
                return false;
            }

            public override int GetHashCode()
            {
                return ((System.Drawing.Rectangle)this).GetHashCode();
            }

            public override string ToString()
            {
                return string.Format(System.Globalization.CultureInfo.CurrentCulture, "{{Left={0},Top={1},Right={2},Bottom={3}}}", Left, Top, Right, Bottom);
            }
        }

        [DllImport("user32.dll")]
